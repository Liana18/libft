#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long int	k;
	char		c;

	k = n;
	if (n == -2147483648)
	{
		ft_putchar_fd('-', fd);
		ft_putchar_fd('2', fd);
		k = 147483648;
	}
	if (k < 0)
	{
		k = -k;
		write (fd, "-", 1);
	}
	c = k % 10 + 48;
	if (k >= 10)
	{
		ft_putnbr_fd(k / 10, fd);
	}
	write (fd, &c, 1);
}
